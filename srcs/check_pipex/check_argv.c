#include "../../pipex.h"

int	check_argv(int argc, char **argv)
{
	int	i;

	i = 0;
	while (i < (argc - 1))
	{
		if (ft_strncmp(argv[i], "", 1) == 0)
			return (0);
		i ++;
	}
	return (1);
}
